package enums;

/**
 * Enumeración que define los tipos de animales en el zoológico.
 * 
 * @author ISC Israel de Jesús Mar Parada (MPFx++)
 * @version 1.0.0
 */
public enum TipoAnimal {
    /** Mamífero (pelo, glándulas mamarias) */
    MAMIFERO,
    /** Ave (plumas, pico, alas) */
    AVE,
    /** Reptil (escamas, sangre fría) */
    REPTIL,
    /** Anfibio (piel húmeda, metamorfosis) */
    ANFIBIO,
    /** Pez (escamas, branquias, acuático) */
    PEZ
}
// fin de la clase TipoAnimal