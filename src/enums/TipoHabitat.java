package enums;

/**
 * Enumeración que define los tipos de hábitat en el zoológico.
 * 
 * @author ISC Israel de Jesús Mar Parada (MPFx++)
 * @version 1.0.0
 */
public enum TipoHabitat {
    /** Sabana africana (pastizales, clima cálido) */
    SABANA,
    /** Jungla tropical (alta humedad, vegetación densa) */
    JUNGLA,
    /** Desierto (árido, temperaturas extremas) */
    DESIERTO,
    /** Hábitat polar (frío extremo, hielo) */
    POLAR,
    /** Hábitat acuático (agua, peces, mamíferos marinos) */
    ACUATICO
}
// fin de la clase TipoHabitat