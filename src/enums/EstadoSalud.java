package enums;

/**
 * Enumeración que representa el estado de salud de un animal.
 * 
 * @author ISC Israel de Jesús Mar Parada (MPFx++)
 * @version 1.0.0
 */
public enum EstadoSalud {
    /** Animal sano, sin problemas */
    SALUDABLE,
    /** Animal requiere atención médica */
    ATENCION,
    /** Animal en estado crítico */
    CRITICO,
    /** Animal en cuarentena por enfermedad */
    EN_CUARENTENA
}
// fin de la clase EstadoSalud